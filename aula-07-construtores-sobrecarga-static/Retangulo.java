class Retangulo {
    // Atributos
    double base;    // base = 0.0
    double altura;  // altura = 0.0

    // Metodos
    public Retangulo() {
        base = 10;
        altura = 10;
    }

    public Retangulo(double base, double altura) {
        this.base = base;
        this.altura = altura;
    }
}

